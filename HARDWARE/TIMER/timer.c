//定时器7中断服务程序		    
void TIM7_IRQHandler(void)
{ 	
	OSIntEnter();    		    
	if(TIM_GetITStatus(TIM7,TIM_IT_Update)==SET)//是更新中断
	{	 			   
		USART3_RX_STA|=1<<15;	//标记接收完成
		TIM_ClearITPendingBit(TIM7,TIM_IT_Update);  //清除中断标志位      
   
		TIM_Cmd(TIM7,DISABLE); 	//关闭定时器7	  
	}	    
	OSIntExit();  											 
} 
